package com.shift105.model;

public class DayProp {

	private int day;
	private String excp_id;
	private String excp_code;
	
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public String getExcp_id() {
		return excp_id;
	}
	public void setExcp_id(String excp_id) {
		this.excp_id = excp_id;
	}
	public String getExcp_code() {
		return excp_code;
	}
	public void setExcp_code(String excp_code) {
		this.excp_code = excp_code;
	}

}
